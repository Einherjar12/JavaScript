/* 10.  Запросить дату (день, месяц, год) и вывести следующую за ней дату.
Учтите возможность перехода на следующий месяц, год, а также високосный год. */

// 10. if/else

let day = Number(prompt("Введите день:"));
let month = Number(prompt("Введите месяц:"));
let y = Number(prompt("Введите год:"));

let daysInMonth;

if (month === 2) {
    daysInMonth = ((y % 400 === 0) || (y % 4 === 0 && y % 100 !== 0))
        ? 29
        : 28;
} else if ([4, 6, 9, 11].includes(month)) {
    daysInMonth = 30;
} else {
    daysInMonth = 31;
}

day++;

if (day > daysInMonth) {
    day = 1;
    month++;
}

if (month > 12) {
    month = 1;
    y++;
}

alert(`Следующая дата: ${day}.${month}.${y}`);
