/* 6. Написать конвертор валют. Пользователь вводит количество
USD, выбирает, в какую валюту хочет перевести: EUR,
UAN илиAZN, и получает в ответ соответствующую сумму.*/

// 6. switch

let usd = +prompt("Введите сумму в USD:");
