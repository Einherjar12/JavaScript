/* 1. Запросить у пользователя его возраст и определить, кем он
является: ребенком (0–2), подростком (12–18), взрослым
(18_60) или пенсионером (60– ...).*/

// 1. if / else if

let age = Number(prompt("Введите ваш возраст:"));

if (age >= 0 && age <= 2) {
    alert("Вы ребенок");
} else if (age >= 12 && age <= 18) {
    alert("Вы подросток");
} else if (age > 18 && age <= 60) {
    alert("Вы взрослый");
} else if (age > 60) {
    alert("Вы пенсионер");
} else {
    alert("Некорректный возраст");
}


/* 2. Запросить у пользователя число от 0 до 9 и вывести ему
спецсимвол, который расположен на этой клавише (1–!, 2–@, 3–# и т. д).*/

// 2. switch

let num = prompt("Введите число от 0 до 9:");

switch (num) {
    case "1": {
        alert("!");
        break;
    }
    case "2": {
        alert("@");
        break;
    }
    case "3": {
        alert("#");
        break;
    }
    case "4": {
        alert("$");
        break;
    }
    case "5": {
        alert("%");
        break;
    }
    case "6": {
        alert("^");
        break;
    }
    case "7": {
        alert("&");
        break;
    }
    case "8": {
        alert("*");
        break;
    }
    case "9": {
        alert("(");
        break;
    }
    case "0": {
        alert(")");
        break;
    }
    default: {
        alert("Введите число от 0 до 9");
    }
}


/* 3. Запросить у пользователя трехзначное число и проверить,
есть ли в нем одинаковые цифры*/

// 3. if

let number = prompt("Введите трехзначное число:");

if (number.length === 3) {
    let a = number[0];
    let b = number[1];
    let c = number[2];

    if (a === b || a === c || b === c) {
        alert("Есть одинаковые цифры");
    } else {
        alert("Все цифры разные");
    }
} else {
    alert("Число не трехзначное");
}


