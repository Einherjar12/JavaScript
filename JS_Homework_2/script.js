/* 7. Запросить у пользователя сумму покупки и вывести сумму к оплате со скидкой:
от 200 до 300 – скидка будет 3%, от 300 до 500 – 5%, от 500 и выше – 7%. */

// 7. if / else

let sum = +prompt("Введите сумму покупки:");
let discount = 0;

if (sum >= 200 && sum < 300) {
    discount = 0.03;
} else if (sum >= 300 && sum < 500) {
    discount = 0.05;
} else if (sum >= 500) {
    discount = 0.07;
}

alert(`К оплате: ${sum - sum * discount}`);


/* 8.  Запросить у пользователя длину окружности и периметр квадрата.
Определить, может ли такая окружность поместиться в указанный квадрат. */

// 8. if

let circleLength = Number(prompt("Введите длину окружности:"));
let squarePerimeter = Number(prompt("Введите периметр квадрата:"));

let radius = circleLength / (2 * Math.PI);
let squareSide = squarePerimeter / 4;

if (radius * 2 <= squareSide) {
    alert("Окружность поместится в квадрат");
} else {
    alert("Окружность не поместится в квадрат");
}


