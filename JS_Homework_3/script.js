/* Задание 1. Подсчитать сумму всех чисел в заданном пользователем диапазоне. */

function task1() {
    let start = parseInt(prompt("Введите начало диапазона:"));
    let end = parseInt(prompt("Введите конец диапазона:"));
    let sum = 0;
    for (let i = start; i <= end; i++) {
        sum += i;
    }
    alert(`Сумма чисел от ${start} до ${end} = ${sum}`);
}

/* Задание 2. Запросить 2 числа и найти только наибольший общий делитель. */

function task2() {
    let a = parseInt(prompt("Введите первое число:"));
    let b = parseInt(prompt("Введите второе число:"));
    while (b !== 0) {
        let temp = b;
        b = a % b;
        a = temp;
    }
    alert(`Наибольший общий делитель = ${a}`);
}

/* Задание 3. Запросить у пользователя число и вывести все делители этого числа. */

function task3() {
    let num = parseInt(prompt("Введите число:"));
    let divisors = [];
    for (let i = 1; i <= num; i++) {
        if (num % i === 0) divisors.push(i);
    }
    alert(`Делители числа ${num}: ${divisors.join(", ")}`);
}

/* Задание 4. Определить количество цифр в введенном числе. */

function task4() {
    let num = prompt("Введите число:");

    if (isNaN(num)) {
        alert("Ошибка! Введите только число.");
        return;
    }

    if (num[0] === "-") {
        num = num.slice(1); // убираем минус
    }

    alert(`Количество цифр в числе: ${num.length}`);
}

/* Задание 5. Запросить у пользователя 10 чисел и подсчитать, сколько
он ввел положительных, отрицательных и нулей. При этом также посчитать,
сколько четных и нечетных. Вывести статистику на экран. Учтите, что
достаточно одной переменной (не 10) для ввода чисел пользователем. */

function task5() {
    let positive = 0, negative = 0, zero = 0, even = 0, odd = 0;

    for (let i = 0; i < 10; i++) {
        let num;

        do {
            num = parseInt(prompt(`Введите число ${i + 1}:`));
            if (isNaN(num)) {
                alert("Ошибка! Нужно ввести число.");
            }
        } while (isNaN(num));

        if (num > 0) positive++;
        else if (num < 0) negative++;
        else zero++;

        if (num % 2 === 0) even++;
        else odd++;
    }

    alert(
        `Положительных: ${positive}\n` +
        `Отрицательных: ${negative}\n` +
        `Нулей: ${zero}\n` +
        `Четных: ${even}\n` +
        `Нечетных: ${odd}`
    );
}

/* Задание 6. Зациклить калькулятор. Запросить у пользователя 2 числа
и знак, решить пример, вывести результат и спросить, хочет ли он решить
еще один пример. И так до тех пор, пока пользователь не откажется. */

function task6() {
    let again;
    do {
        let a = parseFloat(prompt("Введите первое число:"));
        let b = parseFloat(prompt("Введите второе число:"));
        let op = prompt("Введите операцию (+, -, *, /):");
        let result;

        switch(op) {
            case '+': result = a + b; break;
            case '-': result = a - b; break;
            case '*': result = a * b; break;
            case '/': result = b !== 0 ? a / b : "На 0 делить нельзя"; break;
            default: result = "Неверная операция";
        }

        alert(`Результат: ${result}`);
        again = confirm("Хотите решить еще один пример?");
    } while (again);
}

/* Задание 7. Запросить у пользователя число и на сколько цифр
его сдвинуть. Сдвинуть цифрычисла и вывести результат (если
число 123456 сдвинуть на 2 цифры, то получится 345612). */

function task7() {
    let num = prompt("Введите число:");
    let shift = parseInt(prompt("На сколько цифр сдвинуть число?"));
    shift = shift % num.length;
    let result = num.slice(shift) + num.slice(0, shift);
    alert(`Результат после сдвига: ${result}`);
}

