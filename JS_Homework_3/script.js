/* Задание 9. Вывести таблицу умножения для всех чисел от 2 до 9.
Каждое число необходимо умножить на числа от 1 до 10. */

function task9() {
    for (let i = 2; i <= 9; i++) {
        let table = "";
        for (let j = 1; j <= 10; j++) {
            table += `${i} x ${j} = ${i * j}\n`;
        }
        alert(`Таблица умножения для числа ${i}:\n\n${table}`);
    }
}



/* Задание 10. Игра «Угадай число». Предложить пользователю загадать
число от 0 до 100 и отгадать его следующим способом:каждую итерацию
цикла делите диапазон чисел пополам, записываете результат в N и
спрашиваете у пользователя «Ваше число > N, < N или == N?». В зависимости
от того, что указал пользователь, уменьшаете диапазон. Начальный
диапазон от 0 до 100, поделили пополам и получили 50. Если пользователь
указал, что его число > 50, то изменили диапазон на от 51 до 100.
И так до тех пор, пока пользователь не выберет == N. */

function task10() {
    alert("Загадайте число от 0 до 100. Я попытаюсь его угадать!");
    let low = 0;
    let high = 100;
    let guessed = false;

    while (!guessed) {
        let mid = Math.floor((low + high) / 2);
        let answer = prompt(`Ваше число > ${mid}, < ${mid} или == ${mid}? (введите >, <, ==)`);

        if (answer === "==") {
            alert(`Я угадал! Ваше число ${mid}`);
            guessed = true;
        } else if (answer === ">") {
            low = mid + 1;
        } else if (answer === "<") {
            high = mid - 1;
        } else {
            alert("Введите корректный ответ: >, < или ==");
        }
    }
}
