/* Запрашивать данные у пользователя необходимо с помощью
prompt(), а выводить результат с помощью alert().*/

/* 1. Запросите у пользователя его имя и выведите в ответ:
«Привет, его имя!».*/

let name = prompt("Введите ваше имя:");
alert("Привет, " + name + "!");

/* 2. Запросите у пользователя год его рождения, посчитайте,
сколько ему лет и выведите результат. Текущий год укажите
в коде как константу.*/

const CURRENT_YEAR = 2026;
let birthYear = prompt("Введите год рождения:");
let age = CURRENT_YEAR - birthYear;
alert("Вам " + age + " лет");

/* 3. Запросите у пользователя длину стороны квадрата
и выведите периметр такого квадрата. */

let side = prompt("Введите длину стороны квадрата:");
let perimeter = 4 * side;
alert("Периметр квадрата: " + perimeter);

/* 4. Запросите у пользователя радиус окружности и
выведите площадь такой окружности. */

const PI = 3.14159;
let radius = prompt("Введите радиус окружности:");
let area = PI * radius * radius;
alert("Площадь окружности: " + area);

/* 5. Запросите у пользователя расстояние в км между двумя
городами и за сколько часов он хочет добраться. Посчитайте
скорость, с которой необходимо двигаться, чтобы успеть вовремя. */

let distance = prompt("Введите расстояние (км):");
let time = prompt("За сколько часов хотите добраться?");
let speed = distance / time;
alert("Необходимая скорость: " + speed + " км/ч");

/* 6. Реализуйте конвертор валют. Пользователь вводит доллары,
программа переводит в евро. Курс валюты храните в константе. */

const USD_TO_EUR = 0.92;
let dollars = prompt("Введите сумму в долларах:");
let euros = dollars * USD_TO_EUR;
alert("Сумма в евро: " + euros);

/* 7. Пользователь указывает объем флешки в Гб. Программа должна
 посчитать сколько файлов размером в 820 Мб помещается на флешку. */

let flashGb = prompt("Введите объем флешки (Гб):");
let files = Math.floor((flashGb * 1024) / 820);
alert("Файлов поместится: " + files);

/* 8. Пользователь вводит сумму денег в кошельке и цену одной
шоколадки. Программа выводит сколько шоколадок может
купить пользователь и сколько сдачи у него останется. */

let money = prompt("Введите сумму денег:");
let price = prompt("Введите цену шоколадки:");
let count = Math.floor(money / price);
let change = money % price;
alert("Шоколадок: " + count + ", сдача: " + change);

/* 9. Запросите у пользователя трехзначное число и выведите его задом
наперед. Для решения задачи вам понадобится оператор % (остаток от деления). */

let number = prompt("Введите трехзначное число:");
let a = number % 10;
let b = Math.floor(number / 10) % 10;
let c = Math.floor(number / 100);
alert("Число наоборот: " + (a * 100 + b * 10 + c));

/* 10. Запросите у пользователя целое число и выведите
в ответ, четное число или нет. В задании используйте логические
операторы. В задании не надо использовать if или switch. */

let num = prompt("Введите целое число:");
alert("Четное число: " + (num % 2 === 0));
